#include "cub3D.h"

void	free_arr(char **arr)
{
	int	i;

	if (!arr)
		return ;
	i = 0;
	while (arr[i])
		free(arr[i++]);
	free(arr);
}

void	free_all(t_data *vars)
{
	free(vars->img1);
	free(vars->img2);
}